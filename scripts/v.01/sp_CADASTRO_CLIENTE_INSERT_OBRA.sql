		IN `p_CONTRATO_VCTO_APOS_ENTREGA` INT,
		IN `p_CONTRATO_MULTA_MORA` DECIMAL(18,2),
		IN `p_CONTRATO_MULTA_DIA` DECIMAL(18,2),
		IN `p_USUARIO` VARCHAR(10)
	)
	
	LANGUAGE SQL
	NOT DETERMINISTIC
	CONTAINS SQL
	SQL SECURITY DEFINER
	COMMENT ''
BEGIN
 INSERT INTO cadastros
         (
			ID_RELACAO				,
			ENDERECO					,
			NUMERO					,
			COMPLEMENTO				,
			BAIRRO					, 
			CEP                 	,
			CIDADE					,
			ESTADO					,
			NOME_FANTASIA		   ,	
