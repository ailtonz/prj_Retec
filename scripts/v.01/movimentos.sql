CREATE TABLE movimentos (
  idMovimento int(11) NOT NULL AUTO_INCREMENT,
  idRelacao int(11) DEFAULT NULL,
  Movimento varchar(45) DEFAULT NULL,
  Plano varchar(45) DEFAULT NULL,
  Grupo varchar(45) DEFAULT NULL,
  Conta varchar(45) DEFAULT NULL,
  Documento varchar(45) DEFAULT NULL,
  Observacao varchar(45) DEFAULT NULL,
  DataDeEmissao varchar(50) DEFAULT NULL,
  DataDeVencimento varchar(50) DEFAULT NULL,
  ValorOriginal decimal(18,2) DEFAULT NULL,
  DataDePagamento varchar(50) DEFAULT NULL,
  ValorFinal decimal(18,2) DEFAULT NULL,
  FormaDePagamento varchar(45) DEFAULT NULL,
  Controle varchar(45) DEFAULT NULL,
  REGISTRO_INSERT_EMISSAO datetime DEFAULT NULL COMMENT 'RESPONSAVEL PELO REGISTRO - EMISSÃO',
  REGISTRO_INSERT_USUARIO varchar(10) DEFAULT NULL COMMENT 'RESPONSAVEL PELO REGISTRO - USUÁRIO',
  REGISTRO_UPDATE_EMISSAO datetime DEFAULT NULL COMMENT 'ULTIMA ALTERAÇÃO DO REGISTRO - EMISSÃO',
  REGISTRO_UPDATE_USUARIO varchar(10) DEFAULT NULL COMMENT 'ULTIMA ALTERAÇÃO DO REGISTRO - USUÁRIO',
  REGISTRO_CATEGORIA varchar(50) DEFAULT NULL COMMENT 'RECEITA, DESPESA, PLANO DE CONTA',
  PRIMARY KEY (idMovimento)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;